var ctx;
function Start(canvasBody)
{	
	// Create the canvas
	var canvas = document.createElement("canvas");
	if(canvas.getContext)
	{
		ctx = canvas.getContext("2d");
		canvas.width = (map.screen.width * map.tile.width);
		canvas.height = (map.screen.height * map.tile.height);

		var b = document.getElementById(canvasBody);
    	b.appendChild(canvas);

		//everything is drawn by this renderer
		Renderer = new SpriteRenderer(ctx, 
			{
				spritesheet:fileSource,
				spriteData:spriteDef,
				onReady:init
			});

	}
}

function init()
{  
  var now = timestamp();	
	lastTime = now;
	
	gameAsset = new Game(map);
	FixedLoop();  
}

function FixedLoop(){
	now = timestamp();
	dt = dt + Math.min(1, (now - lastTime) / 1000);
	while (dt > step) {
	  dt = dt - step;
	  update(step);
	}

	render();
				
	lastTime = now;
	rf(FixedLoop);	
}

function timestamp() {
	var wp = window.performance;
	return wp && wp.now ? wp.now() : new Date().getTime();
}

// Update game objects
function update(dt) {
	gameAsset.Update(dt);
};

function render() {	
	gameAsset.Render();
};

window.onload = function() {
	Start("canvasBody");
}
